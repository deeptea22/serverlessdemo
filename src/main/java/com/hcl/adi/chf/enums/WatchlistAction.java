package com.hcl.adi.chf.enums;

/**
 * Enumeration to define watchlist action that any clinician can perform for any
 * patient, i.e. a clinician can add a patient into the watchlist or can delete
 * a patient from the watchlist
 *
 * @author AyushRa
 */
public enum WatchlistAction {
	ADD, DELETE;
}